//Imperative vs Declarative

//Imperative:- giving instruction on how to do things step by step
//Example:- Computer,jquery

for (let index = 0; index < 100; index++) {
	console.log(index);
}

//Declarative:- telling programs todo things but only saying what to do? , not how to do it
//Example:- Humans,React

[1, 2, 3].forEach((item) => console.log(item));
